def operacao(num1, num2, simbolo):
    if simbolo == "+":
        calculo = num1 + num2
        return calculo
    elif simbolo == "-":
        calculo = num1 - num2
        return calculo
    elif simbolo == "*":
        calculo = num1 * num2
        return calculo
    elif simbolo == "/":
        calculo = num1 / num2
        return calculo